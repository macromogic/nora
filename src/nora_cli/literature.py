"""nora literature — structured literature backend (Stage 3).

papers.yaml is the single source of truth (see papers.py). Markdown files
under .nora/literature/ are generated views. State transitions go through
'mark', which enforces the decision gate mechanically: promoting a paper to
a gated status requires an approved entry in .nora/decisions/decisions.yaml.

This is the first argparse subtree in the CLI; the legacy hand-written
dispatch for other commands is intentionally untouched.
"""

from __future__ import annotations

import argparse
import difflib
import re
from datetime import date
from pathlib import Path

from . import papers as ps
from . import sources as src

HELP_TEXT = """\
Nora literature manager

Commands:
  nora literature init      Create .nora/literature/ with an empty papers.yaml
  nora literature search    Query external sources (--query "..."; arXiv/OpenAlex/S2/Crossref/DBLP,
                            keyless, cached permanently, --refresh to re-fetch); hits enter candidate state
  nora literature ingest    Add candidate papers (--bibtex FILE | --titles FILE | --title ... manual entry)
  nora literature dedup     Report likely duplicates in papers.yaml (never merges automatically)
  nora literature queue     List papers in the reading queue (status queued/reading)
  nora literature mark      Change a paper's status, roles (--role), or note (--note);
                            gated statuses need --decision <approved id>
  nora literature coverage  Compact role x status coverage report
  nora literature render    Regenerate READING_QUEUE.md and RELATED_WORK_MAP.md from papers.yaml
  nora literature doctor    Check literature state (papers.yaml parses, decision gate consistent)

Statuses: candidate -> queued -> reading -> read -> proposed_cite -> cited / rejected
Promotions to read/proposed_cite/cited/rejected require an approved decision
(.nora/decisions/decisions.yaml) passed via 'mark --decision'.

Judgment work (what to read, why it matters) lives in the skill:
  /nora-literature-manager triage | reading-queue | paper-note | related-work-map | search"""

LEGACY_FILES = ["LITERATURE_LOG.md", "READING_QUEUE.md", "RELATED_WORK_MAP.md"]

GENERATED_HEADER = "<!-- Generated by 'nora literature render' from papers.yaml. Do not edit by hand. -->"

# Conservative fuzzy-title threshold: only near-identical titles qualify,
# and they are reported as review candidates, never merged.
FUZZY_THRESHOLD = 0.93


def _today() -> str:
    return date.today().isoformat()


def _base(explicit: Path | None = None) -> Path:
    from .cli import find_workspace_root
    return explicit or find_workspace_root() or Path.cwd()


def _lit_dir(base: Path) -> Path:
    return base / ".nora" / "literature"


def read_decisions(base: Path) -> dict:
    """Loose scan of decisions.yaml: id -> status. Comment lines ignored."""
    path = base / ".nora" / "decisions" / "decisions.yaml"
    decisions: dict = {}
    if not path.is_file():
        return decisions
    current = None
    for line in path.read_text().splitlines():
        stripped = line.strip()
        if stripped.startswith("#"):
            continue
        m = re.match(r"-\s+id:\s*(\S+)", stripped)
        if m:
            current = m.group(1)
            continue
        m = re.match(r"status:\s*(\S+)", stripped)
        if m and current:
            decisions[current] = m.group(1)
    return decisions


